package Haustier;

public class Haustier 
{
	public String name;
	public int alter;
	
	public Haustier(String name, int alter) 
	{
		this.name = name;
		this.alter = alter;
	}

	public String getName()
	{
		return name;
	}
	
	public int getAlter()
	{
		return alter;
	}
	
	public void zeigen()
	{
		System.out.print("ERROR");
	}
	
	public void geraeuschMachen()
	{
		System.out.println("ERROR");
	}
	
	public String toString()
	{
		return "ERROR";
	}
}
